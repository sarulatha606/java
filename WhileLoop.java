package javaprogram;

public class WhileLoop {

	
		
	 public static void main(String[] args) {
	        int i = 10; 
	        while (i <= 100) { 
	            System.out.print(i); 
	            if (i < 100) { 
	                System.out.print(", ");             }
	            i += 10; 
	        }
	    }

	}


